class PostData {
  final String title;
  final String imagePath;
  final String profileImagePath;
  final int likes;
  final String comments;
  final String description;

  PostData({
    required this.title,
    required this.imagePath,
    required this.profileImagePath,
    required this.likes,
    required this.comments,
    required this.description,
  });
}
